// --------- While Loop ------------
let i = 0;
while (i <= 15) {
  console.log(i);
  i += 2;
}

// another example with an array

let myArray = [12, 34, 56, 78];
let arr = 0;
while (arr < myArray.length) {
  console.log(myArray[arr]);
  arr++;
}

// ---------- do while loop -----------------
let score = 1;
do{
  console.log(`Score is ${score}`);
  score++;
}while(score <= 20);

